    private void btn_SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SalvarActionPerformed
        if (txt_id.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Digite o id do Cliente.");
                txt_id.requestFocus();
        }else{
            try {
                int id = Integer.parseInt(txt_id.getText());

                jpa.conexaoAberta();
                Cliente cliente_encontrado = jpa.find_Cliente_by_id(id);
                if(cliente_encontrado.getId() == id){
                    Venda venda = new Venda(cliente_encontrado);
                    venda.setData(new Date());
                    venda.setValor_Total(Double.parseDouble(txt_valor.getText()));
                    jpa.persist(venda);
                    JOptionPane.showMessageDialog(null, "Venda cadastrada com sucesso!");
                }
                
            } catch (Exception e) {
                Logger.getLogger(Tela_Cliente.class.getName()).log(Level.SEVERE, null, e);
                JOptionPane.showMessageDialog(null, "Informe um id v√°lido!");
            }
            jpa.fecharConexao();
        }
        
            
    }//GEN-LAST:event_btn_SalvarActionPerformed

