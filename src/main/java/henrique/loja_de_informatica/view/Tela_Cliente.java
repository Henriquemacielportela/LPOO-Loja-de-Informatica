    public void carregarClienteCadastrados() {
        jpa.conexaoAberta();

        DefaultListModel modeloLista = new DefaultListModel();
        modeloLista.addAll(jpa.getClientes());
        lista_Clientes.setModel(modeloLista);

        jpa.fecharConexao();

    }

