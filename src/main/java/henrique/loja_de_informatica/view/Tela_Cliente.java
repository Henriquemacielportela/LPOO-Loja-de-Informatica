    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        Cliente cliente_selecionado = lista_Clientes.getSelectedValue();
        if (cliente_selecionado != null) {
            Tela_Cadastro_Cliente telaEdt = new Tela_Cadastro_Cliente(this, rootPaneCheckingEnabled);
            telaEdt.setCliente(cliente_selecionado);
            telaEdt.setVisible(true);
            
            
            carregarClienteCadastrados();
            
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um cliente para editar.");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void Remover_ClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Remover_ClienteActionPerformed
        Cliente cliente_selecionado = lista_Clientes.getSelectedValue();
        if (cliente_selecionado != null) {
            System.out.println("Cliente: " + cliente_selecionado.getTipo_Cliente());
            try {
                jpa.conexaoAberta();

                int delOp = JOptionPane.showConfirmDialog(this,
                        "Tem certeza que deseja remover " + cliente_selecionado.getNome() + "?");
                if (delOp == JOptionPane.YES_OPTION) {
                    jpa.remover(cliente_selecionado);
                }

                jpa.fecharConexao();
                carregarClienteCadastrados();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this,
                        "Erro ao remover cliente " + cliente_selecionado + "\n" + e);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione uma pessoa para remover");
        }
    }//GEN-LAST:event_Remover_ClienteActionPerformed

    private void txtBusca_NomeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBusca_NomeKeyReleased
        if(txtBusca_Nome.getText().trim().isEmpty()){
            carregarClienteCadastrados();
        } else{
            jpa.conexaoAberta();
            DefaultListModel modeloLista = new DefaultListModel();
            modeloLista.addAll(jpa.getClientes(txtBusca_Nome.getText().trim()));
            System.out.println("Pessoas carregadas: "+jpa.getClientes(txtBusca_Nome.getText().trim()));
            lista_Clientes.setModel(modeloLista);

            jpa.fecharConexao();
        }
    }//GEN-LAST:event_txtBusca_NomeKeyReleased

